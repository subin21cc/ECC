# ----------------------------------------------------------
# [5장] 깃허브로 협업하기 & 원격 브랜치 정보 가져오기
# ----------------------------------------------------------

# 1. 원격 저장소 복제 (git clone)
# git clone https://github.com/사용자아이디/저장소명.git .

# 2. 원격 저장소의 최신 정보 확인 (git fetch)
# 워킹 디렉토리는 건드리지 않고 정보만 가져옴
git fetch origin

# 3. 원격 저장소의 변경사항 병합 (git pull)
# fetch + merge 와 동일
git pull origin main

# 4. 새로운 브랜치 생성 및 원격에 푸시
git checkout -b feature-login
echo "login function" > login.txt
git add login.txt
git commit -m "Add login logic"
git push -u origin feature-login


